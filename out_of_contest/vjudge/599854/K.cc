int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n;
    cin >> n;

    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
        a[i] %= 2;
    }

    vector dp(N, vector(2, vector<Z>(3)));
    dp[0][0][0] = 1;
    Z ans = 0;
    for (int x : a) {
        auto ndp = dp;
        for (int i = 0; i < N; i++) {
            for (int s = 0; s < 2; s++) {
                for (int r = 0; r < 3; r++) {
                    if (dp[i][s][r] == 0) {
                        continue;
                    }
                    if (i + 1 == N) {
                        if ((r + x) % 2 == 0) {
                            ans += dp[i][s][r];
                        }
                        ndp[i][r - s][x + r - s] += dp[i][s][r];
                    } else {
                        if (i == 0) {
                            ndp[i + 1][x][r + x] += dp[i][s][r];
                        } else {
                            ndp[i + 1][s][r + x] += dp[i][s][r];
                        }
                    }
                }
            }
        }
        dp = move(ndp);
    }

    cout << ans << "\n";
}

